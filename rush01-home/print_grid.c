#include "rush01.h"
#include <unistd.h>

void	print_grid(int grid[N][N])
{
	int		i;
	int		j;
	char	c;

	i = 0;
	while (i < N)
	{
		j = 0;
		while (j < N)
		{
			c = grid[i][j] + '0';
			write(1, &c, 1);
			if (j != N - 1)
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
